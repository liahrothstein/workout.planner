export function setBicepsClassName(exercise: string): string {
    switch (exercise) {
        case 'Поочередный подъем гантелей на бицепс':
            return ('biceps alternateLiftingOfDumbbellsOnBiceps');
            break;
        case 'Сгибание на бицепс в кроссовере рукоятка':
            return ('biceps bicepFlexionInTheCrossoverGrip');
            break;
        case 'Сгибание на бицепс рукояткой в кроссовере':
            return ('biceps bicepFlexionWithTheHandleInTheCrossover');
            break;
        case 'Подъем на бицепс в кроссовере':
            return ('biceps bicepLiftInACrossover');
            break;
        case 'Подъем на бицепс сидя в тренажёре':
            return ('biceps bicepLiftingWhileSittingInTheTrainer');
            break;
        case 'Подъем на бицепс сидя Молоток':
            return ('biceps bicepLiftSittingHammer');
            break;
        case 'Концентрированные сгибание на бицепс сидя':
            return ('biceps concentratedFlexionOnBicepsWhileSitting');
            break;
        case 'Сгибание на предплечья':
            return ('biceps flexionOnTheForearms');
            break;
        case 'Сгибание на предплечья (сидя)':
            return ('biceps flexionOnTheForearmsSitting');
            break;
        case 'Сгибание на предплечья сидя пронированный хват':
            return ('biceps flexionOnTheForearmsWhileSittingWithAPiercedGrip');
            break;
        case 'Подъемы на предплечья пронированным хватом':
            return ('biceps forearmLiftsWithAThreadedGrip');
            break;
        case 'Молотковые подъемы (поочередно)':
            return ('biceps hammerLiftsAlternately');
            break;
        case 'Подъем штанги на бицепс':
            return ('biceps liftingABarbellOnBiceps');
            break;
        case 'Подъем гантели на бицепс в скамье Скотта нейтральным хватом':
            return ('biceps liftingADumbbellOnABicepInAScottBenchWithANeutralGrip');
            break;
        case 'Подъем штанги на бицепс на наклонной скамье':
            return ('biceps liftingBarbellsOnBicepsOnAnInclinedBench');
            break;
        case 'Подъем на бицепс в тренажёре пронированный хват':
            return ('biceps liftingBicepsInTheTrainerWithAThreadedGrip');
            break;
        case 'Подъем гантели на бицепс наклонная скамья':
            return ('biceps liftingDumbbellOnBicepsInclinedBench');
            break;
        case 'Подъем гантели на бицепс в скамье Скотта':
            return ('biceps liftingDumbbellOnBicepsInScottsBench');
            break;
        case 'Подъем гантели на бицепс, на наклонной скамье':
            return ('biceps liftingDumbbellOnBicepsOnAnInclinedBench');
            break;
        case 'Подъем гантели на бицепс сидя в скамье Скотта':
            return ('biceps liftingDumbbellOnBicepsWhileSittingOnScottsBench');
            break;
        case 'Подъем гантелей на бицепс':
            return ('biceps liftingDumbbellsOnBiceps');
            break;
        case 'Подъем гантелей на бицепс Молоток':
            return ('biceps liftingDumbbellsOnBicepsHammer');
            break;
        case 'Подъем гантелей на бицепс Молоток поочередные сидя':
            return ('biceps liftingDumbbellsOnBicepsHammerAlternateSitting');
            break;
        case 'Подъем гантелей на бицепс Молоток наклонная скамья':
            return ('biceps liftingDumbbellsOnBicepsHammerInclinedBench');
            break;
        case 'Подъем гантелей на бицепс наклонная скамья':
            return ('biceps liftingDumbbellsOnBicepsInclinedBench');
            break;
        case 'Подъем гантелей на бицепс соревновательный вариант':
            return ('biceps liftingDumbbellsOnBicepsIsACompetitiveOption');
            break;
        case 'Подъем гантелей на бицепс сидя поочередный':
            return ('biceps liftingDumbbellsOnBicepsWhileSittingAlternately');
            break;
        case 'Подъем гантелей на бицепс прямым хватом':
            return ('biceps liftingDumbbellsOnBicepsWithAStraightGrip');
            break;
        case 'Подъем штанги на бицепс в скамье Скотта Е образный гриф':
            return ('biceps liftingTheBarbellOnTheBicepsInTheScottBenchEBar');
            break;
        case 'Подъем штанги на бицепс в скамье Скотта прямым грифом':
            return ('biceps liftingTheBarbellOnTheBicepsInTheScottBenchWithAStraightBar');
            break;
        case 'Подъем штанги на бицепс соревновательный вариант':
            return ('biceps liftingTheBarbellOnTheBicepsIsACompetitiveOption');
            break;
        case 'Подъем штанги на бицепс прямым грифом':
            return ('biceps liftingTheBarbellOnTheBicepsWithAStraightBar');
            break;
        case 'Подъем штанги на бицепс прямым хватом':
            return ('biceps liftingTheBarbellOnTheBicepsWithAStraightGrip');
            break;
        case 'Подъем штанги на бицепс прямым хватом Е образный гриф':
            return ('biceps liftingTheBarbellOnTheBicepsWithAStraightGripEShapedBar');
            break;
        case 'Подъем штанги на бицепс широкий хват':
            return ('biceps liftingTheBarbellOnTheBicepsWithAWideGrip');
            break;
        case 'Подъем на бицепс канатной рукоятки в кроссовере':
            return ('biceps liftingTheBicepsOfTheRopeHandleInTheCrossover');
            break;
        case 'Подъем Е образной штанги на бицепс сидя':
            return ('biceps liftingTheEShapedBarbellOnTheBicepsWhileSitting');
            break;
        case 'Паучьи сгибания на бицепс':
            return ('biceps spiderFlexionOnBiceps');
            break;
        case 'Паучьи сгибания на бицепс пронированным хватом':
            return ('biceps spiderFlexionOnBicepsWithAPiercedGrip');
            break;
        default:
            return ('biceps zottmanBicepLift');
            break;
    }
}